    private void DrugName(String nameOrUniqueID){
       DefaultTableModel model = (DefaultTableModel) drugTable.getModel(); 
       model.setRowCount(0);
       try{
           Connection con= ConnectionProvider.getCon();
           Statement st=con.createStatement();
           ResultSet rs=st.executeQuery("select * from Drug where Drug_Name like '"+nameOrUniqueID+"%'or Drug_ID like'"+nameOrUniqueID+"%'");
           while(rs.next()){
               model.addRow(new Object[]{rs.getString("drug_ID")+"- "+rs.getString("Drug_Name")});
           }
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
    }
