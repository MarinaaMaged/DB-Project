    private void addCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCartActionPerformed
        // TODO add your handling code here:
        String noOfUnits=txtNoOfUnits.getText();
        String drugID=txtDrugID.getText();
        
        if(!noOfUnits.equals("")&&!drugID.equals("")){
            String drugName=txtName.getText();
            String drugmanf=txtdrugmanfucture.getText();
            String price=txtPrice.getText();
            String total=txtTotalPrice.getText();
            int checkStock=0;
            int checkDrugExist=0;
            
            try{
               Connection con=ConnectionProvider.getCon();
           Statement st=con.createStatement();
           ResultSet rs=st.executeQuery("select * from drug where drug_ID="+drugID+"");
            while(rs.next()){
                if(rs.getInt("stock")>=Integer.parseInt(noOfUnits)){
                    checkStock=1;
                }else{
                    JOptionPane.showMessageDialog(null, "Drug is out of stock. Only "+rs.getInt("stock")+"left");
                }
            }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            if(checkStock==1){
                DefaultTableModel dtm=(DefaultTableModel) cartTable.getModel();
                if(cartTable.getRowCount()!=0){
                    for (int i = 0; i < cartTable.getRowCount(); i++) {
                       if(Integer.parseInt(dtm.getValueAt(i, 0).toString())== Integer.parseInt(drugID)){
                           checkDrugExist=1;
                           JOptionPane.showMessageDialog(null, "medicine already exists in cart");
                       }
                    }
                }
                if(checkDrugExist==0){
                    dtm.addRow(new Object[]{drugID,drugName,drugmanf,price,noOfUnits,total});
                    finalTotalPrice=finalTotalPrice +Integer.parseInt(total);
                    finalPrice.setText(String.valueOf(finalTotalPrice));
                    JOptionPane.showMessageDialog(null, "Added Successfuly");
                }
                clearDrugFields();
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Number of Units and drug ID are required");
        }
    }//GEN-LAST:event_addCartActionPerformed

